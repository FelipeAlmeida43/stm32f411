#define WS2812_DATA_PIN GPIO_PIN_8
#define WS2812_GPIO_PORT GPIOA
//#define NUM_LEDS 4
uint32_t millisLed = 0;
void delay_ns(uint32_t ns) {
    uint32_t cycles = (72 * ns) / 10; // Convert ns to clock cycles for 72 MHz
    while (cycles--);
}

void WS2812SendBit(uint8_t bit) {
    if (bit) {
        // Send '1'
        WS2812_GPIO_PORT->BSRR = WS2812_DATA_PIN;  // Set pin high
        delay_ns(900); // 0.9 microseconds high

        WS2812_GPIO_PORT->BSRR = (uint32_t)WS2812_DATA_PIN << 16; // Reset pin low
        delay_ns(350); // 0.35 microseconds low
    } else {
        // Send '0'
        WS2812_GPIO_PORT->BSRR = WS2812_DATA_PIN;  // Set pin high
        delay_ns(350); // 0.35 microseconds high

        WS2812_GPIO_PORT->BSRR = (uint32_t)WS2812_DATA_PIN << 16; // Reset pin low
        delay_ns(900); // 0.9 microseconds low
    }
}

void WS2812SendByte(uint8_t byte) {
    for (int i = 0; i < 8; i++) {
        WS2812SendBit((byte << i) & 0x80);
    }
}

void WS2812SendColor(uint8_t r, uint8_t g, uint8_t b) {
    WS2812SendByte(g); // Send green byte
    WS2812SendByte(r); // Send red byte
    WS2812SendByte(b); // Send blue byte
}

void WS2812SendColors(uint8_t colors[][3], uint8_t num_leds) {
    for (uint8_t i = 0; i < num_leds; i++) {
        WS2812SendColor(colors[i][0], colors[i][1], colors[i][2]);
    }
}
